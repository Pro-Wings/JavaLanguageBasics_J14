package com.prowings.corejava.loops;

public class WhileDemo {
	
	public static void main(String[] args) {
		
		System.out.println("main method started!!");
		
		int i = 6;
		
		while(i>=0)
		{
			System.out.println(i);
			i--;
		}
		
		System.out.println("main method ended!!");
		
	}

}
